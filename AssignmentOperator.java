public class AssignmentOperator {
    public static void main (String agrs[]){
        int a;
        a=10;    //Simple assignment Operator
        System.out.println(a);
        a+=10;    //Compound assignment Operator (a=a+10)
        System.out.println(a);
        a-=10;    //Compound assignment Operator (a=a-10)
        System.out.println(a);
        a*=10;    //Compound assignment Operator (a=a*10)
        System.out.println(a);
        a/=10;    //Compound assignment Operator (a=a/10)
        System.out.println(a);
        a%=10;    //Compound assignment Operator (a=a%10)
        System.out.println(a);
    }
}
