public class PrePostIncrementDecrement {
    public static void main (String arg[]){
        int a=10;
        System.out.println(a);

        System.out.println("PrePost Incr.....");
        System.out.println(a++);
        System.out.println(++a);

        System.out.println("PrePost Decr.....");
        System.out.println(a--);
        System.out.println(--a);
    }
}
